Route::group([
    'prefix' => 'auth'
], function ($router) {
    Route::post('/login', [AuthController::class, 'login']);
    Route::post('/register', [AuthController::class, 'register']);
    Route::post('/logout', [AuthController::class, 'logout'])->middleware('auth:api');
    Route::post('/refresh', [AuthController::class, 'refresh'])->middleware('auth:api');
    Route::get('/profile', [AuthController::class, 'userProfile'])->middleware('auth:api');
    Route::post('/change-pass', [AuthController::class, 'changePassWord'])->middleware('auth:api');
});


Route::resource('products', ProductController::class)->middleware('auth:api');
